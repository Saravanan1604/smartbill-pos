// Current language — defaults to English, persisted in localStorage
let _lang = localStorage.getItem('smartbill_lang') || 'en';

// Translation function — window.t('key') used everywhere
window.t = function(key) {
  return (TRANSLATIONS[_lang] && TRANSLATIONS[_lang][key]) || TRANSLATIONS['en'][key] || key;
};

export function getLang() { return _lang; }

export function setLang(lang) {
  if (!TRANSLATIONS[lang]) return;
  _lang = lang;
  localStorage.setItem('smartbill_lang', lang);
  document.documentElement.lang = lang === 'ta' ? 'ta' : 'en';
}

