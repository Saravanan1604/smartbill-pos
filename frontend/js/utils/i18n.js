// Current language — defaults to English, persisted in localStorage
let _lang = localStorage.getItem('smartbill_lang') || 'en';

// Translation function — window.t('key') used everywhere
window.t = function(key) {
  return (TRANSLATIONS[_lang] && TRANSLATIONS[_lang][key]) || TRANSLATIONS['en'][key] || key;
};

export function getLang() { return _lang; }

export function setLang(lang) {
  if (!TRANSLATIONS[lang]) return;
  _lang = lang;
  localStorage.setItem('smartbill_lang', lang);
  document.documentElement.lang = lang;
}

export function getAvailableLangs() {
  return [
    { code: 'en', label: 'English', flag: '🇬🇧' },
    { code: 'ta', label: 'தமிழ்',   flag: '🇮🇳' },
    { code: 'hi', label: 'हिन्दी',   flag: '🇮🇳' },
  ];
}

// Returns the next language in the cycle en → ta → hi → en
export function nextLang(current = _lang) {
  const order = ['en', 'ta', 'hi'];
  const idx = order.indexOf(current);
  return order[(idx + 1) % order.length];
}

// Initialize immediately
document.documentElement.lang = _lang;
